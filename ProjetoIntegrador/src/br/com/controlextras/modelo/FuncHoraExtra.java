package br.com.controlextras.modelo;

public class FuncHoraExtra {
    private int id;
    private int heId;
    private int funcId;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getHeId() {
        return heId;
    }

    public void setHeId(int horaExtraId) {
        this.heId = horaExtraId;
    }

    public int getFuncId() {
        return funcId;
    }

    public void setFuncId(int funcId) {
        this.funcId = funcId;
    }
}
