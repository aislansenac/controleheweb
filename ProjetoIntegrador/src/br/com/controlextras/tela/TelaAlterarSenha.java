    private void btnAlteraSenhaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAlteraSenhaActionPerformed
        // TODO add your handling code here:
        if(!emptyFields()) {
            Funcionario funcionario = TelaInicial.funcionario;
            funcionario.setCpf(txtSenha.getText());
            char[] charSenha = pswNovaSenha.getPassword();
            String senha = new String(charSenha);
            funcionario.setSenha(senha);
            
            FuncionarioDAO funcDao = new FuncionarioDAO();
            funcDao.editar(funcionario);
            JOptionPane.showMessageDialog(null, "Senha atualizada com sucesso.");
            this.dispose();

        }
    }//GEN-LAST:event_btnAlteraSenhaActionPerformed

